fn main() {

    // print variants
    println!("Hello, world!");

    print!("hello, world!");

    print!("\n");

    print!("The value is {}",10);

    print!("\n");

    // carriage return \r 
    print!("This is not displayed \r this will be displayed");

    print!("\n");

    println!("I am {1} happy to do {0}","this","very");

    println!("I am {arg1} happy to do {arg2}",arg1="very",arg2="this");
}
